def maximumPerimeterTriangle(sticks):
    # Write your code here
    sticks = sorted(sticks, reverse=True)
    for idx in range(len(sticks) - 2):
        if sticks[idx] < sticks[idx+1] + sticks[idx+2]:
            return [sticks[idx+2], sticks[idx+1], sticks[idx]]
    return [-1]

